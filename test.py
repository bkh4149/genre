
"""集計：まずは各ジャンルの問題が何問ずつあるのか集計
たとえば、ars=[["雑学","経済"],["雑学","数学","地理"],["雑学","経済"]]から
  dc={'雑学': 3, '経済': 2, '数学': 1, '地理': 1}をつくりたい
"""

ars=[["雑学","経済"],
["雑学","数学","地理"],
["雑学","経済"]]


dc={}
for ar in ars:
  for ar1 in ar:
    if ar1 in dc:
      dc[ar1]+=1
    else:
      dc[ar1]=1
print(dc)        